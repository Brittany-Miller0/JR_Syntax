package One_JavaSyntax.task.task_4.task_038;

/*
 * Рисуем линии
 * for вывести линии из 10 (8)
 * Вертикально и горизонтально
 */

public class Solution {
    public static void main(String[] args) {
        for (int x = 1; x <= 9; x++) System.out.print("8");
        for (int y = 0; y <= 10; y++) System.out.println("8");
    }
}
